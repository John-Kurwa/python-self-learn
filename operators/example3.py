# comparison operators
x = 5
y = 3
# ==	Equal	x == y	
print(x == y)
# !=	Not equal	x != y	
print(x != y)
# >	Greater than	x > y	
print(x > y)    
# <	Less than	x < y	
print(x < y)
# >=	Greater than or equal to	x >= y	
print(x >= y)
# <=	Less than or equal to	x <= y
print(x <= y)

# chained comparison
m = 10
print(2 < m < 15)
print(2 < m and m <15)
